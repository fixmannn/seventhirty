  <h3>Cara Pembayaran</h3>
  <h4>ATM MANDIRI</h4>
  <ol>
    <li>Masukkan kartu ATM dan PIN.</li>
    <li>Pilih menu "Bayar/Beli".</li>
    <li>Pilih menu "Multipayment".</li>
    <li>Ketik kode perusahaan, yaitu "88608" / "88908" (Xendit 88608 / "88908"), tekan "BENAR".</li>
    <li>Masukkan nomor Virtual Account.</li>
    <li>Isi nominal, kemudian tekan "BENAR".</li>
    <li>Pilih nomor "1", kemudian tekan "YA".</li>
    <li>Akan muncul konfirmasi pembayaran, lalu pili tombol "YA".</li>
    <li>Simpan struk sebagai bukti pembayaran anda.</li>
  </ol>
  <h4>MANDIRI INTERNET BANKING / LIVIN' BY MANDIRI</h4>
  <ol>
    <li>Login Livin' By Mandiri dengan memasukkan Username dan Password.</li>
    <li>Pilih menu "Pembayaran".</li>
    <li>Pilih menu "Multi Payment".</li>
    <li>Pilih penyedia jasa "Xendit 88608" / "Xendit 88908".</li>
    <li>Pilih "No. Virtual", kemudian masukan nomor Virtual Account Anda</li>
    <li>Masukkan "Nominal" lalu pilih "Konfirmasi"</li>
    <li>Pilih "Lanjut".</li>
    <li>Muncul tampilan konfirmasi pembayaran, lalu pilih "Konfirmasi".</li>
    <li>Masukkan "PIN" dan transaksi telah selesai</li>
  </ol>
</div>